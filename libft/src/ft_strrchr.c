#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char *save;

	save = (char *)0;
	while (1)
	{
		if (*s == c)
			save = (char *)s;
		if (*s++ == 0)
			break ;
	}
	return (save);
}
